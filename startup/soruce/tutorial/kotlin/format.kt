fun main(args: Array<String>) {
    
    // 計算の基本値として利用する
    var baseValue = 120;
    
    // 文字フォーマット内加算の例
    println("add value ${baseValue + 1}")

    // 文字フォーマット内減算の例
    println("sub value ${baseValue - 2 }")

    // 文字フォーマット内積の例
    println("mul value ${baseValue * 3}")

    // 文字フォーマット内割り算の例
    println("division value ${baseValue / 2}")

    
}
